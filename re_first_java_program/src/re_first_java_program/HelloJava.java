package re_first_java_program;

/**
 * Java Core Programming 1-1
 */
public class HelloJava {
	/**
	 * public static void main(String[] args){...}
	 * 자바 코드 실행 명령어입니다.
	 * static은 인스턴스를 호출하지 않고 사용하는 명령어입니다.
	 * Class명은 HelloJava (CapitalCase)
	 * 변수명은 helloJava (camelCase)
	 * @param args 매개변수
	 */
	public static void main(String[] args) {
		System.out.println("Hello Java World!");
	}
}
